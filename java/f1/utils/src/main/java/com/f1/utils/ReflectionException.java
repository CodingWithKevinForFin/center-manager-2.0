package com.f1.utils;

public class ReflectionException extends RuntimeException {

	public ReflectionException() {
		super();
	}

	public ReflectionException(String message_, Throwable cause_) {
		super(message_, cause_);
	}

	public ReflectionException(String message_) {
		super(message_);
	}

	public ReflectionException(Throwable cause_) {
		super(cause_);
	}

}

