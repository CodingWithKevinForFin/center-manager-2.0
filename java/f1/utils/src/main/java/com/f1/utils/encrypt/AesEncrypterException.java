package com.f1.utils.encrypt;

public class AesEncrypterException extends RuntimeException {

	public AesEncrypterException() {
		super();
	}

	public AesEncrypterException(String message, Throwable cause) {
		super(message, cause);
	}

	public AesEncrypterException(String message) {
		super(message);
	}

	public AesEncrypterException(Throwable cause) {
		super(cause);
	}

}
