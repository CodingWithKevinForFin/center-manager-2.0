package com.f1.container.exceptions;

public class PartitionResolverException extends ContainerException {

	public PartitionResolverException() {
		super();
	}

	public PartitionResolverException(String message, Throwable cause) {
		super(message, cause);
	}

	public PartitionResolverException(String message) {
		super(message);
	}

	public PartitionResolverException(Throwable cause) {
		super(cause);
	}

}
