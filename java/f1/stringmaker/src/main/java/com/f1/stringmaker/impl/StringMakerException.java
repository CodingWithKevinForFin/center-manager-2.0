package com.f1.stringmaker.impl;

import com.f1.utils.DetailedException;

public class StringMakerException extends DetailedException {

	public StringMakerException() {
		super();
	}

	public StringMakerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StringMakerException(String arg0) {
		super(arg0);
	}

	public StringMakerException(Throwable arg0) {
		super(arg0);
	}

}
