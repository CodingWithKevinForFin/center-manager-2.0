package com.f1.speedlogger;

public class SpeedLoggerException extends Exception {

	public SpeedLoggerException() {
		super();
	}

	public SpeedLoggerException(String message, Throwable cause) {
		super(message, cause);
	}

	public SpeedLoggerException(String message) {
		super(message);
	}

	public SpeedLoggerException(Throwable cause) {
		super(cause);
	}

}
