package com.f1.ami.amicommon;

public class AmiScmException extends Exception {

	public AmiScmException() {
		super();
	}

	public AmiScmException(String message, Throwable cause) {
		super(message, cause);
	}

	public AmiScmException(String message) {
		super(message);
	}

	public AmiScmException(Throwable cause) {
		super(cause);
	}

}
