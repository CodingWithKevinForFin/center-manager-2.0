package com.f1.ami.sim;

public class AmiSimException extends RuntimeException {

	public AmiSimException() {
		super();
	}

	public AmiSimException(String message, Throwable cause) {
		super(message, cause);
	}

	public AmiSimException(String message) {
		super(message);
	}

	public AmiSimException(Throwable cause) {
		super(cause);
	}

}
