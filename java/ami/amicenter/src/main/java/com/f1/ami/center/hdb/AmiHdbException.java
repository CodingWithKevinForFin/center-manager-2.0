package com.f1.ami.center.hdb;

public class AmiHdbException extends RuntimeException {

	public AmiHdbException() {
	}

	public AmiHdbException(String message, Throwable cause) {
		super(message, cause);
	}

	public AmiHdbException(String message) {
		super(message);
	}

	public AmiHdbException(Throwable cause) {
		super(cause);
	}

}
