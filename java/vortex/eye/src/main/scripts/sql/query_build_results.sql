    verify_data_length as verifyDataLength,
    verify_data_vvid as verifyDataVvid,
    data_checksum as dataChecksum,
    verify_data_checksum as verifyDataChecksum,
    start_time as startTime,
    metadata as '(STRINGMAP)metadata',
    state
