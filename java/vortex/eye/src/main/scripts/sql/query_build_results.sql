    substring(build_stderr,1,1024) as buildStderr,
    build_stderr_length as buildStderrLength,
    build_stderr_vvid as buildStderrVvid,
    substring(data,1,1024) as data,
    data_length as dataLength,
    data_vvid as dataVvid,
